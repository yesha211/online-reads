function setBookData(bookName,bookDesc,author,published,bookImage,LetsReadLink){
    const bookData = {
        bookName,
        bookDesc,
        author,
        published,
        bookImage,
        LetsReadLink
    }
    localStorage.setItem("BookData",JSON.stringify(bookData));
}